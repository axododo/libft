#include "libft.h"

char	*ft_strdup(const char *src)
{
	int i;
	char *dest;

	dest = (char *)malloc(ft_strlen(src) + 1);
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}