#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t			i;
	char unsigned	*str;

	i = 0;
	str = (unsigned char *)s;
	while (i < n)
	{
		str[i] = 0;
		i++;
	}
	return (s);
}
